/**
 * element plus 的文件大小 Formatter 实现
 *
 * @param row 行数据
 * @param column 字段
 * @param cellValue 字段值
 */
// @ts-ignore
export const fileSizeFormatter = (row, column, cellValue) => {
  const fileSize = cellValue
  const unitArr = ['Bytes', 'KB', 'MB', 'GB', 'TB', 'PB', 'EB', 'ZB', 'YB']
  const srcSize = parseFloat(fileSize)
  const index = Math.floor(Math.log(srcSize) / Math.log(1024))
  const size = srcSize / Math.pow(1024, index)
  const sizeStr = size.toFixed(2) //保留的小数位数
  return sizeStr + ' ' + unitArr[index]
}

/**
 * 根据身份证号计算年龄
 */
export function calculateAge(idCard: string) {
  if (!idCard || idCard.length === 0) return '-'
  const birthday = idCard.substring(6, 14)
  const year = +birthday.substring(0, 4)
  const month = +birthday.substring(4, 6)
  const day = +birthday.substring(6, 8)
  const date = new Date(year, month - 1, day)
  const age = (new Date().getTime() - date.getTime()) / (365 * 24 * 60 * 60 * 1000)
  return Math.floor(age)
}

/**
 * 根据身份证计算性别
 */
export function getGender(idCard: string) {
  const genderCode = +idCard.substring(16, 17)
  return genderCode % 2 === 0 ? '女' : '男'
}
//加法函数，用来得到精确的加法结果
export const accAdd = (arg1: any, arg2: any) => {
  let r1: any, r2: any
  try {
    r1 = arg1.toString().split('.')[1].length
  } catch (e) {
    r1 = 0
  }
  try {
    r2 = arg2.toString().split('.')[1].length
  } catch (e) {
    r2 = 0
  }
  const m = Math.pow(10, Math.max(r1, r2))
  return (arg1 * m + arg2 * m) / m
}

//减法函数，用来得到精确的加法结果
export const accSub = (arg1: any, arg2: any) => {
  let r1: any, r2: any
  try {
    r1 = arg1.toString().split('.')[1].length
  } catch (e) {
    r1 = 0
  }
  try {
    r2 = arg2.toString().split('.')[1].length
  } catch (e) {
    r2 = 0
  }
  const m = Math.pow(10, Math.max(r1, r2))
  //last modify by deeka
  //动态控制精度长度
  const n = r1 >= r2 ? r1 : r2
  return ((arg1 * m - arg2 * m) / m).toFixed(n)
}

//乘法函数，用来得到精确的乘法结果
//说明：javascript的乘法结果会有误差，在两个浮点数相乘的时候会比较明显。这个函数返回较为精确的乘法结果。
//调用：accMul(arg1,arg2)
//返回值：arg1乘以arg2的精确结果
export const accMul = (arg1: any, arg2: any) => {
  let m = 0
  const s1 = arg1.toString()
  const s2 = arg2.toString()
  try {
    m += s1.split('.')[1].length
  } catch (e) {}
  try {
    m += s2.split('.')[1].length
  } catch (e) {}
  return (Number(s1.replace('.', '')) * Number(s2.replace('.', ''))) / Math.pow(10, m)
}

//除法函数
export const accDiv = (arg1: any, arg2: any) => {
  let t1 = 0,
    t2 = 0
  try {
    t1 = arg1.toString().split('.')[1].length
  } catch (e) {}
  try {
    t2 = arg2.toString().split('.')[1].length
  } catch (e) {}
  const r1 = Number(arg1.toString().replace('.', ''))
  const r2 = Number(arg2.toString().replace('.', ''))
  return (r1 / r2) * Math.pow(10, t2 - t1)
}

// 获取月份最后一天
export const getLastDayByMonth = (year, month) => {
  const date = new Date(year, month)

  date.setMonth(date.getMonth() + 1)
  date.setDate(0)

  return date.getDate()
}
